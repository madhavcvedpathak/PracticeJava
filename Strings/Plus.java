package Strings;

public class Plus {
    public static void main(String[] args) {
        String s = "bhakti ";
        s = s + "hi shakti hai !!";
        s += 10;
        s += 'a';
        System.out.println("raghav"+10+20);
        System.out.println(10+20+"raghav");
        System.out.println(10+"raghav"+20);
        System.out.println(10+20+"gopi");
        System.out.println("raghav"+(10+20));
        System.out.println(s);
    }
}
